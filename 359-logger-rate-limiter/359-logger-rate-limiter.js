
var Logger = function() {
    this.log = {};
};

/** 
 * @param {number} timestamp 
 * @param {string} message
 * @return {boolean}
 */
Logger.prototype.shouldPrintMessage = function(timestamp, message) {
    return this.log?.[message] > timestamp ? false : !!(this.log[message] = timestamp + 10)
};

